    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        People people = new People();
        PeopleRegister reg = new PeopleRegister();
        try {
        people.setBirthDate(TxtBornDate.getText());
        people.setZipCode(TxtCEP.getText());
        people.setCpf(TxtCPF.getText());
        people.setRoad(TxtRoad.getText());
        people.setTelephone(TxtTelephone.getText());
        people.setNumber(Integer.parseInt(TxtNumber.getText()));
        people.setEmail(TxtEmail.getText());
        people.setCity(TxtCity.getText());
        people.setDistrict(txtDistrict.getText());
        people.setSex(CbxSex.getSelectedItem().toString());
        people.setState(CbxCountry.getSelectedItem().toString());
        people.setRg(TxtRG.getText());
        people.setName(TxtName.getText());
        reg.save(people);
        View.setVisible(true);
        Register.setVisible(false);    
        } catch (Exception ex) {
            Logger.getLogger(ScreenUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnSaveActionPerformed


    private void RefreshUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RefreshUserActionPerformed
       
       try {
            //Atualizar a tabela de usuÃ¡riop
            ClassShare ClassShare = new ClassShare();
            ResultSet RS = ClassShare.findAll("Person");
            DefaultTableModel modelo = (DefaultTableModel) ScreenUser.tbReg.getModel();
            modelo.setNumRows(0);
            int columns = RS.getMetaData().getColumnCount();
            while (RS.next()) {
                Object[] row = new Object[columns];
                for(I = 0; I < columns; I++){
                    row[I-1]= RS.getObject(I);
                } 
                ((DefaultTableModel)tbReg.getModel()).insertRow(RS.getRow()-1, row);
            }
       }catch (SQLException ex) { 
                 
                 }                 
    }//GEN-LAST:event_RefreshUserActionPerformed
